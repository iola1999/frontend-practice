function search(nums: number[], target: number): number {
  let split,
    last = nums[0];
  const n = nums.length;
  for (let i = 0; i < n; i++) {
    if (last > nums[i]) {
      split = i;
    }
    last = nums[i];
  }
  let arr: number[];
  if (split === undefined) return binSearch(nums, target);
  if (target < nums[n - 1]) {
    arr = nums.slice(split, n);
    const index = binSearch(arr, target);
    return index === -1 ? -1 : split + index;
  } else if (target === nums[n - 1]) {
    return n - 1;
  } else {
    arr = nums.slice(0, split);
    const index = binSearch(arr, target);
    return index === -1 ? -1 : index;
  }
}

function binSearch(nums: number[], target: number): number {
  let left = 0;
  let right = nums.length - 1;
  while (left <= right) {
    const middle = left + Math.floor((right - left) / 2); // é¿å… l+r æº¢å‡º
    if (nums[middle] === target) {
      return middle;
    } else if (nums[middle] > target) {
      right = middle - 1;
    } else {
      left = middle + 1;
    }
  }
  return -1;
}

